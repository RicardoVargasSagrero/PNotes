    private void Combo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Combo1ActionPerformed
        // TODO add your handling code here:
        metodoBusqueda = Combo1.getSelectedItem().toString();
        if(metodoBusqueda.contentEquals("Número de nota")){
            busqueda = JOptionPane.showInputDialog("Ingrese un número de nota");
        }else if(metodoBusqueda.contains("Nombre del cliente")){
            busqueda = JOptionPane.showInputDialog("Ingrese un nombre de cliente");
        }else if(metodoBusqueda.contains("Fecha del evento")){
            busqueda = JOptionPane.showInputDialog("Ingrese una fecha de evento\n**FORMATO YYYY-MM-DD");
        }
        String choice = null;
        System.out.println(choice);
        if(metodoBusqueda == null){
            JOptionPane.showMessageDialog(null, "Seleccione un método de búsqueda");
            return;
        }
        if(busqueda == null){
            return;
        }
        switch(Combo1.getSelectedIndex()){
            case 0:
                choice = "SELECT id_Cliente,cliente,domicilio,num_casa,fecha_evento FROM cliente WHERE id_Cliente = '"+busqueda+"';";
                System.out.println("id_Cliente: "+busqueda);
                break;
            case 1:
                //choice = "SELECT id_Cliente,cliente,domicilio,num_casa,fecha_evento FROM cliente WHERE cliente = '"+busqueda+"';";
                choice = "SELECT id_Cliente,cliente,domicilio,num_casa,fecha_evento FROM cliente WHERE cliente LIKE '%"+busqueda+"%';";
                System.out.println("cliente: "+busqueda);
                break;
            case 2:
                choice = "SELECT id_Cliente,cliente,domicilio,num_casa,fecha_evento FROM cliente WHERE fecha_evento = '"+busqueda+"';";
                System.out.println("Fecha de evento: "+busqueda);
                break;
            default: 
                JOptionPane.showMessageDialog(null, "Error");
        }
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            mostrartabla(choice);
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        } catch (SQLException ex) {
            Logger.getLogger(Busqueda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Combo1ActionPerformed

