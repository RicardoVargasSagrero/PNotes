    void mostrartabla(String sql) throws SQLException{
        /*Conexión a la base de datos*/
        coneccion conA = new coneccion();
        Connection con = conA.getConection();
        /*Formato de la tabla*/
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Número de nota");
        modelo.addColumn("Nombre del Cliente");
        modelo.addColumn("Domicilio");
        modelo.addColumn("Número de Casa");
        modelo.addColumn("Fecha Pedido");
        tabladatos.setModel(modelo);
        /*Query para la consulta*/
        //String sql = "SELECT id_Cliente,cliente,domicilio,num_casa,fecha_evento FROM cliente;";
        /*Arreglo par la información guardada*/
        String datos[] = new String[5];
        try{
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                modelo.addRow(datos);
            }
            tabladatos.setModel(modelo);
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
